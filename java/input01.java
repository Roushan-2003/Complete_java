// Hello world Input

public class input01 {
    public static void main(String[] args){
        //prints text on the console
        System.out.println("Hello World");

    }
}

// public class HelloWorld → Class name.
// main() → Entry point of every Java program.
// System.out.println() → Prints output.



/*
public: An access modifier. It makes the method globally visible so the JVM can execute it from outside the class.

static: A keyword that associates the method with the class itself, rather than instances of the class. This allows the JVM to call the method without having to create an object first.

void: Indicates that the method does not return any value.

main: The specific name of the method. It acts as an identifier so the JVM knows exactly where to start execution.

String[] args: An array of String objects used to accept command-line arguments. It allows external data to be passed to your program at runtime


 */